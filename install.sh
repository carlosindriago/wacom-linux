#!/bin/bash
# 🎨 Wacom Linux Tool - Universal Multi-Device Installer (TUI Edition)
# Supports ANY Wacom Tablet (X11) - Optimized for Multi-Monitor setups

set -e

# Colors for non-TUI output
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
NC='\033[0m'

SETTINGS_FILE="$HOME/.wacom_settings.env"
TITLE="Wacom Universal Linux Customizer"

# --- DEPENDENCY CHECK ---
if ! command -v whiptail &> /dev/null; then sudo apt update && sudo apt install -y whiptail; fi
if ! command -v xsetwacom &> /dev/null; then sudo apt update && sudo apt install -y xserver-xorg-input-wacom; fi
if ! command -v xrandr &> /dev/null; then sudo apt update && sudo apt install -y x11-xserver-utils; fi

# --- TUI WIZARD ---
whiptail --title "$TITLE" --msgbox "Bienvenido al configurador UNIVERSAL de Wacom.\n\nEste asistente configurará automáticamente CUALQUIER tableta Wacom conectada y optimizará tu setup de monitores." 12 70

# 1. Orientation
HAND_OPT=$(whiptail --title "$TITLE" --menu "Selecciona tu mano dominante:" 12 60 2 \
    "1" "ZURDO (Rotación 180°)" \
    "2" "DIESTRO (Sin rotación)" 3>&1 1>&2 2>&3)

case $HAND_OPT in
    1) ROTATION="half" ;;
    *) ROTATION="none" ;;
esac

# 2. Button 2 (Lower)
BTN2_OPT=$(whiptail --title "$TITLE" --menu "Acción para el BOTÓN INFERIOR (Stylus):" 12 60 2 \
    "1" "Click Derecho (Normal)" \
    "2" "Click Medio (Scroll)" 3>&1 1>&2 2>&3)

case $BTN2_OPT in
    2) BTN2_ACT="2" ;;
    *) BTN2_ACT="3" ;;
esac

# 3. Button 3 (Upper)
BTN3_OPT=$(whiptail --title "$TITLE" --menu "Acción para el BOTÓN SUPERIOR (Stylus):" 12 60 2 \
    "1" "Modo Toggle (Tableta/Mouse)" \
    "2" "Deshacer (Ctrl+Z)" 3>&1 1>&2 2>&3)

case $BTN3_OPT in
    2) BTN3_ACT="key +ctrl +z -z -ctrl" ;;
    *) BTN3_ACT="key F12" ;;
esac

# 4. ADVANCED MULTI-MONITOR MAPPING
# Generar lista de pantallas dinámicamente
SCREENS=$(xrandr --listactivemonitors | grep "+" | awk '{print $4}')
SCREEN_LIST="ALL Escritorio_Completo" # Opción inicial para mapear a todo

for s in $SCREENS; do
    SCREEN_LIST="$SCREEN_LIST $s Monitor_Individual"
done

SCREEN_NAME=$(whiptail --title "$TITLE" --menu "Mapeo de Pantalla:\n¿A qué monitor quieres vincular tu tableta?" 15 70 6 $SCREEN_LIST 3>&1 1>&2 2>&3)

if [ -z "$SCREEN_NAME" ]; then SCREEN_NAME="ALL"; fi

# Save settings
cat <<EOF > "$SETTINGS_FILE"
# Wacom Settings generated by TUI installer (Advanced)
ROTATION="$ROTATION"
BUTTON_2="$BTN2_ACT"
BUTTON_3="$BTN3_ACT"
SCREEN="$SCREEN_NAME"
PRESSURE_CURVE="0 20 80 100"
EOF

# --- SYSTEM INSTALLATION ---

# 1. Copiar scripts
cp .wacom_config.sh .wacom_toggle.sh .wacom_udev_trigger.sh "$HOME/"
chmod +x "$HOME"/.wacom_*.sh

# 2. Regla udev GENÉRICA (Vendor ID 056a para cualquier Wacom)
echo -e "\n${BLUE}[2/3] Configurando persistencia udev GENÉRICA...${NC}"
RULE_PATH="/etc/udev/rules.d/99-wacom.rules"
# Usamos el Vendor ID de Wacom 056a para capturar CUALQUIER modelo
echo 'ACTION=="add", SUBSYSTEM=="usb", ATTR{idVendor}=="056a", RUN+="'"$HOME"/.wacom_udev_trigger.sh'"' | sudo tee "$RULE_PATH" > /dev/null
sudo udevadm control --reload-rules && sudo udevadm trigger

# 3. Autostart Universal
mkdir -p "$HOME/.config/autostart"
cat <<EOF > "$HOME/.config/autostart/wacom-config.desktop"
[Desktop Entry]
Type=Application
Exec=$HOME/.wacom_config.sh
Hidden=false
NoDisplay=false
X-GNOME-Autostart-enabled=true
Name=Wacom Universal Config
Comment=Apply custom Wacom settings on login
EOF

# Final instructions
whiptail --title "🎉 ¡TODO LISTO!" --msgbox "Tu Wacom ha sido configurada.\n\nMonitor asignado: $SCREEN_NAME\n\nSi cambias de modelo de Wacom, el sistema la reconocerá automáticamente al conectarla." 15 70
